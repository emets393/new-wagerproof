// Utility to get contrasting text color for readability
export const getContrastingTextColor = (primaryColor: string, secondaryColor: string): string => {
  const hexToLuminance = (hex: string): number => {
    const rgb = parseInt(hex.slice(1), 16);
    const r = (rgb >> 16) & 0xff;
    const g = (rgb >> 8) & 0xff;
    const b = (rgb >> 0) & 0xff;
    return (0.299 * r + 0.587 * g + 0.114 * b) / 255;
  };

  const primaryLum = hexToLuminance(primaryColor);
  const secondaryLum = hexToLuminance(secondaryColor);
  const avgLuminance = (primaryLum + secondaryLum) / 2;

  return avgLuminance < 0.5 ? '#ffffff' : '#000000';
};

